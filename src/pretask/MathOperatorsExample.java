package pretask;

public class MathOperatorsExample {

    public static void main(String[] args){

        int x = 7;
        int y = 9;

        int sum = x + y;
        int minus = y - x;
        int mult = x * y;
        int div = y / x;

        System.out.println("Sum :" + sum);
        System.out.println("Subtraction :" + minus);
        System.out.println("Multiplication :" + mult);
        System.out.println("Division :" + div);

    }
}
