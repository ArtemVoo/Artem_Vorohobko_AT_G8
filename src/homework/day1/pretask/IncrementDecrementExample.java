package homework.day1.pretask;

public class IncrementDecrementExample {

    public static void main(String[] args) {

        int x = 8;

        ++x;
        System.out.println(x);
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);
        --x;
        System.out.println(x);
    }
}
