package basetask;

public class TrainMethodsPrimitive {

    public void printInt(int x){

        System.out.println("я получил на вход число :" +x);

    }

    public void printLong(long y){

        System.out.println("я получил на вход длинное число :" +y);

    }

    public void printChar(char z){

        System.out.println("я получил на вход символ  :" +z);

    }

    public void printFloat(float q){

        System.out.println("я получил на вход дробное число  :" +q);

    }

    public void printDouble(double e){

        System.out.println("я получил на вход длинное дробное число  :" +e);

    }

    public void printShort(short t){

        System.out.println("я получил на вход короткое число  :" +t);

    }

    public void printByte(byte v){

        System.out.println("я получил на вход очень короткое число  :" +v);

    }

    public void printBoolean(boolean n){

        System.out.println("я получил на вход булево  :" +n);
    }
}
